# Faça um programa que leia o comprimento do cateto oposto e do cateto adjacente de um triângulo regutângulo; calcule e mostre o comprimeiro da hipotenusa.

#exemplo 1:
co = float(input('Comprimento do cateto oposto: '))
ca = float(input('Comprimento do cateto adjacente: '))
hi = (co ** 2 + ca ** 2) ** (1/2)
print('A hipotenusa vai medir {:.2f}'.format(hi))

#exemplo 2 + simples

import math
co = float(input('Comprimento do cateto oposto: '))
ca = float(input('Comprimento do cateto adjacente: '))
hi = math.hypot(co, ca)
print('A hipotenusa vai medir {:.2f}'.format(hi))

